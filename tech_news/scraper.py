import requests
import time
from parsel import Selector


# Requisito 1
def fetch(url):
    try:
        response = requests.get(url, timeout=3)
        time.sleep(1)
        if response.status_code == 200:
            html_content = response.text
            return html_content
        else:
            return None
    except requests.Timeout:
        return None


